# function with more than 1 input
# if u shuffle the data , than obviously data will be shuffled as per parameters

def greet_with(name, location):
    print(f"Hello my name is {name}")
    print(f"i am from the {location}")

greet_with("ndiwakar", "tirupathi")

# for exact resolution of the variables with correct values , we can use this method to skip switching confusion
greet_with(name = "ndiwakar" , location = "tirupathi")

